
public class CalExample {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// 1.대입
		int x =10;
		int y = 2;
		
		// 2.산술연산
		// +, -, /, *, %(나머지)
		int result = 0;
		result = x + y;
		System.out.println("x + y = " + result);
		
		result = x - y;
		System.out.println("x - y = " + result);
		
		result = x * y;
		System.out.println("x * y = " + result);
		
		result = x / y;
		System.out.println("x / y = " + result);
		
		result = x % y;
		System.out.println("x % y = " + result);
		
		// 대입연산자가 산술연산자랑 같이 쓰이는 경우
		int z = 10;
		z = z + 20;	// z += 20;
		z = z - 20; // z -= 20;
		z = z * 20; // z *= 20;
		z = z / 20; // z /= 20;
		z = z % 20; // z %= 20;
		
		// 증감연산자
		// ++, --, 단항연산자
		int x1 = 10;
		++ x1; // x1 = x1 + 1;
		-- x1; // x1 = x1 - 1;
		
		int z1 = 10;
		System.out.println(z1++); // 10 출력
		
		int z2 = 10;
		System.out.println(++z2); // 11 출력
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
