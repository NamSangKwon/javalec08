
public class MultiExample {

	public static void main(String[] args) {
		
		// 구구단
		// x * y = ( x * y )
		System.out.println("## 구구단 ##");
		for (int x = 1; x < 10; x ++) {
			System.out.print("");
			for(int y = 2; y < 10; y++) {
				System.out.print(y+" * " + x +" = " + x*y +"\t");
			}
			System.out.print("\n");
		}

		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
